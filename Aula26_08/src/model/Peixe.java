package model;

public class Peixe extends Animal {
	
	private int aquario;
	private double ind_agua;
	private String data_limpeza;
	private int elementos;
	
	public int getAquario() {
		return aquario;
	}
	public void setAquario(int aquario) {
		this.aquario = aquario;
	}
	public double getInd_agua() {
		return ind_agua;
	}
	public void setInd_agua(double ind_agua) {
		this.ind_agua = ind_agua;
	}
	public String getData_limpeza() {
		return data_limpeza;
	}
	public void setData_limpeza(String data_limpeza) {
		this.data_limpeza = data_limpeza;
	}
	public int getElementos() {
		return elementos;
	}
	public void setElementos(int elementos) {
		this.elementos = elementos;
	}
	

}
